package thirdhomework;

public class Arithmetic {
    public static void main(String[] args) {
        System.out.println(add(1,2));
        System.out.println(add(3,4));
        System.out.println(add(5,6));
    }
    static int add(int a, int b) {
        int sum;
        sum = a + b;
        return sum;
    }
}
